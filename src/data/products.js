/** Look up a product by slug. Returns undefined if not found. */
export function getProductBySlug(slug) {
  return products.find(p => p.slug === slug)
}

/** Get related products from a list of slugs (filters out missing ones). */
export function getRelatedProducts(slugs) {
  return slugs.map(s => products.find(p => p.slug === s)).filter(Boolean)
}
